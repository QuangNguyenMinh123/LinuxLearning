/*******************************************************************************/
static void Nokia5110_Write(Nokia5110Type *device, bool isCommand, char* buff, int size)
{
	if (isCommand)						/* If command is sent */
		gpiod_set_value(dcPin, LOW);
	else								/* If data is sent */
	{
		gpiod_set_value(dcPin, HIGH);
	}
	spi_write(device->nokia5110, buff, size);
	gpiod_set_value(dcPin, LOW);
}

static void Nokia5110_Reset(void)
{
	gpiod_set_value(resetPin, LOW);
	mdelay(100);
	gpiod_set_value(resetPin, HIGH);
}

static void Nokia5110_goto(Nokia5110Type *device, int x, int y)
{
	int xVal = (x <= MAX_ROW) ? (x) : (MAX_ROW);
	int yVal = (y <= MAX_COL) ? (y) : (MAX_COL);
	char buff[3] = {
		0b00100000 | POWER_ON | HORIZONTAL_ADDRESS | BASIC_INSTRUCTION,
		0b10000000 | xVal,		/* Set x address, 0 <= x <= 83*/
		0b01000000 | yVal		/* Set y address, 0 <= y <= 5*/
	};
	Nokia5110_Write(device, true, buff, sizeof(buff));
	device->col = xVal;
	device->row = yVal;
}

static void Nokia5110_Nextline(Nokia5110Type *device)
{
	if (device->row + 1 <= MAX_ROW)
		Nokia5110_goto(device, 0, device->row + 1);
	else
		Nokia5110_goto(device, 0, 0);
}

static void Nokia5110_printChar(Nokia5110Type *device, char ch)
{
	if (ch == '\n')
		Nokia5110_Nextline(device);
	else
	{
		if (nokia5110.col + FONT_SIZE < MAX_COL)
		{
			nokia5110.col += FONT_SIZE;
		}
		else
		{
			if (nokia5110.row + 1 > MAX_ROW)
			{
				Nokia5110_goto(device, 0, 0);
			}
			else
			{
				Nokia5110_goto(device, 0, device->row + 1);
			}
		}
		Nokia5110_Write(device, false, LCD_Font5x7[ch], FONT_SIZE);
	}

}

static void Nokia5110_print(Nokia5110Type *device, char* ch)
{
	while (*ch != 0)
	{
		Nokia5110_printChar(device, *ch);
		ch++;
	}
}

static void Nokia5110_printImage(Nokia5110Type *device, char* ch, int size)
{
	Nokia5110_goto(device, 0, 0);
	Nokia5110_Write(device, false, ch, size);
}

static void Nokia5110_ClearScreen(Nokia5110Type *device)
{
	char buff[504] = {0};
	Nokia5110_goto(device, 0, 0);
	Nokia5110_Write(device, false, buff, 504);
}

static void Nokia5110_Init(Nokia5110Type *device)
{
	char buff[] = {
		0x21,		/* Function set */
		0xC0,
		0x20,		/* Basic command */
		NORMAL
	};
	Nokia5110_Reset();
	Nokia5110_Write(device, true, buff, sizeof(buff));
}
