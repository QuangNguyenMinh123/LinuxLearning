void ILI9341_Nextline(ILI9341Type *device)
{
	if (device->row + 1 <= MAX_ROW)
		ILI9341_goto(device, 0, device->row + 1);
	else
		ILI9341_goto(device, 0, 0);
}

void ILI9341_ClearScreen(ILI9341Type *device)
{
	ILI9341_goto(device, 0, 0);
}

